		<h2 class="featurette-heading">History of the Links</h2>
		<hr class="featurette-divider">
		<h2 class="featurette-heading"><span class="text-muted">Origins</span></h2>
        <p class="lead">
It was a long time ago that golf was first played at Carnoustie. No one is quite sure when the first shots were struck, but the Panmure Register records that some time around 1527, Sir Robert Maule 'exercisit the gowf' on the Links at Carnoustie. Little is known of the course on which Sir Robert played, but it is recorded that the 'wad-fie' (stakes) were for drink. Sound familiar? There is no reason to doubt that golf continued on the Links in the years that followed, but no records were kept.		</p>
		

		<br />
		<h2 class="featurette-heading"><span class="text-muted">1800 Onwards</span></h2>
		
		<p class="lead">
By the early 19th Century we know that a 10-hole course had been established. The starting point was considerably to the east of the present first tee of the Championship Course - perhaps on the area of flat ground immediately to the west of today's tennis courts. 		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">Early Professional Tournaments</span></h2>
		
		<p class="lead">
The holes were comparatively short and led out towards 'The Gulley. There were only 'give way greens, each being used twice in the course of the 10-hole round. On these 'Taymouth' Links, the legendary 'Young' Tom Morris, as a lad of 16, won one of his first professional tournaments. A number of similar open meetings were held as well as several big money challenge matches, but information about these is sketchy. 		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">Carnoustie and Taymouth Club</span></h2>
		
		<p class="lead">
Meanwhile, the residents of Carnoustie continued to enjoy their golf, and by 1842 the aptly named 'Carnoustie and Taymouth Club' was formed. The forerunner of today's Carnoustie Golf Club, it had no clubhouse - that was not to come until 1898. But we can safely say that regular, organised golf was played on the ground - or part of it - that now houses the Carnoustie Medal Course.		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">The Course Designers</span></h2>
		
		<p class="lead">
The ground itself was part of the Dalhousie Estate and the Earl was perfectly happy to allow golf to be played on it. The 10-hole course, some say, had been laid out by Alan Robertson of St Andrews. Others attribute it to a Mr Chambers, head of an Edinburgh publishing house. Be that as it may, by 1867 Carnoustie's golfers were beginning to take the game more seriously. They engaged another famous St Andrean, Old Tom Morris, to extend the course to 18 holes.
		</p>

		<br />
		<h2 class="featurette-heading"><span class="text-muted">Dalhousie Golf Club</span></h2>
		
		<p class="lead">
In October of that year, a meeting in Dundee's 'Royal British Hotel' formed the Dalhousie Golf Club, which for the next 20 or so years was largely to assume responsibility for the course. The birth of the Caley, the outcome of a meeting held in the back premises of a local tailor's shop, was not to come until 1887 - but more about that elsewhere.
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">Government Considers Buying the Links</span></h2>
		
		<p class="lead">
In 1891 - consternation! News filtered through to the members of the Dalhousie Club that the government was contemplating the purchase of the golf course land from the Earl of Dalhousie for military purposes. The Dalhousie Club swiftly negotiated the purchase of the ground. The sale was almost completed when Carnoustie's Burgh Commissioners - the forerunners of the Town Council - discovered what was afoot. 
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">The Town Takes Ownership of the Links</span></h2>
		
		<p class="lead">
The matter was brought up by Commissioner Douglas Colquhoun at a meeting held in a shop in the High Street. After a brief discussion, it was agreed that the golf course should become the property of Carnoustie rather than of a private golf club and the Commissioners managed to convince both Dalhousie Golf Club and the Estate Factor of the rightness of their case. 
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">How the Money was Raised to Buy the Links</span></h2>
		
		<p class="lead">
The result was an agreement that 176 acres of land should be purchased by the town for the sum of £1,350 [in today's money] on condition only that it should be maintained for all time as a golf course. To raise the sum, a mammoth 3-day bazaar was arranged, top be held in Dundee's Kinnaird Hall, on 24, 25 and 26 March 1892. Donations of cash and articles to be sold were sought, and poured in from all over the world, as well as from local golfing enthusiasts. 
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">1918 Onwards</span></h2>
		
		<p class="lead">
In the years after the First World War, Carnoustie really came into its own as a golfing centre. Under the chairmanship of James A. Wright, the Golf Course Management Committee began to dream ambitious dreams.
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">James Braid and the 'Craws Nest Tassie'</span></h2>
		
		<p class="lead">
James Braid was engaged in 1926 to improve the 'Old' Course, the Medal. While sticking essentially to the existing lay-out he re-positioned some of the greens and tees, created some 60 new bunkers and generally tightened up' the circuit. At 6,680 yards, the course produced by Braid was a formidable test of golf, but still not the challenging monster that we know today. To mark its opening in 1927, the now highly popular 'Craws Nest Tassie' tournament was instituted.
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">Carnoustie's First Open</span></h2>
		
		<p class="lead">
1930 brought the Scottish Amateur Championship to Carnoustie for the first time and two days after the final the Royal and Ancient announced the course had been chosen as venue for the Open in 1931. This gave rise to doubts as to whether Carnoustie would provide an adequate test for the world's top golfers. 
		</p>
		
		<p class="lead">
		The last five holes of the Medal Course today are recognised as perhaps the most challenging finish in championship golf. But in 1930 that was not the case. The 'Spectacles' and 'Lucky Slap' were there, but in 1930 they were on the 13th and 14th respectively, instead of the 14th and 15th. 
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">Changes to the Course</span></h2>
		
		<p class="lead">
The old 15th hole was slightly longer than today's 16th, but played to a cupped green instead of a raised one. The 16th was what is now the 17th, but shorter. The 17th in 1930 was a short hole, playing from west to east to just short of where the present 18th hole crosses the Barry Burn for the first time. The 18th was pretty well what was left to today's last hole. It was a 4-4-3-4 finish with holes measuring 339, 335, 160 and 365 yards respectively - all of them likely to produce 'birdies'.
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">1931 Open</span></h2>
		
		<p class="lead">
Prior to the 1931 Open, the present day short 13th was introduced, creating space for the masochistic pleasures which Caley members and others today endure on the Medal's notorious finish. The course itself won wide acclaim during the 1931 Open and Carnoustie found itself on the crest of a golfing wave. Summer visitors flocked to the town. 
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">1937 Open and Henry Cotton</span></h2>
		
		<p class="lead">
Then came the 1937 Open. No alteration was made to the Medal prior to the Championship. But after it, at the suggestion of the winner, Henry Cotton, the 16th green was raised to form the long, narrow and highly inaccessible plateau that confronts the player today.
		</p>
		
		<p class="lead">
		After the war there were to be more big golfing occasions at Carnoustie - Eric Brown's Scottish Amateur Championship in 1947, Ben Hogan's Open in 1953, Gary Player's in 1968, Tom Watson's in 1975 and Paul Lawrie's in 1999. A bunker was added here, a hole slightly altered there, at the suggestions of the great men, but no dramatic alterations were made to the two courses
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">The Burnside gets a Makeover</span></h2>
		
		<p class="lead">
In preparation for the 1937 Open, it was the Burnside that came in for the attention of the golf architects, who set out to turn it into a suitable course for the Open qualifying rounds - played, at that time, in the week of the Championship. 14 new greens were laid, using 15,000 square yards of turf, and 23 new tee-heads were constructed. In addition, the two infamous short holes, the 5th and the 9th, were created. So, as World War Two approached, Carnoustie golfers had the choice of two formidable courses on which to play.
		</p>
		
		<br />
		<h2 class="featurette-heading"><span class="text-muted">Peter Allis Designs the Buddon Course</span></h2>
		
		<p class="lead">
Then in 1978, Angus District Council, who had assumed control of the courses at the time of local government reorganisation, announced that they had appointed ex-Ryder Cup golfer, Peter Alliss, to design a third course at Carnoustie. So came into being the Buddon Course, built on land acquired from the MOD and with each hole ominously and prophetically named after a famous battle.
		</p>
		
